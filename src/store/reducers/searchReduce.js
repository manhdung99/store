const searchReducer = (state = initState, action) => {
  switch (action.type) {
    case "SEARCH":
      let newData;
      let searchValue = action.payload.toUpperCase();
      if (action.payload === "") {
        newData = [];
      } else {
        newData = initState.items.map((item) => {
          return item.name.toUpperCase().includes(searchValue) ? item : null;
        });
      }
      let newItems = [...newData];
      newItems = newItems.filter((newCartItem) => newCartItem !== null);
      return {
        ...state,
        searchItems: newItems,
      };
    case "MOVE_TO_ITEM": {
      const newData = { ...action.payload };
      return {
        ...state,
        detailItem: newData,
      };
    }
    default:
      return state;
  }
};

export { searchReducer };
